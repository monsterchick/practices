public class BooleanValues {
    static int num1;
    static int num2;

    public static void main(String[] args) {
        num1 = 10;
        num2 = 99;
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num1==num2);
        System.out.println(num1>num2);
        System.out.println(num1<num2);
    }
}
